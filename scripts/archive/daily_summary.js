function getTodayDate() {
  const now = new Date();
  return now.toISOString().split('T')[0]; // YYYY-MM-DD
}

function getDisplayDate(dateStr) {
  const [year, month, day] = dateStr.split('-');
  return `${year}年${parseInt(month)}月${parseInt(day)}日`;
}

function readTodayMemory(date) {
  // 搵 latest memory file for this date (e.g., 2026-03-14-2332.md)
  const globPattern = `${date}-*.md`;
  const files = fs.readdirSync(MEMORY_DIR)
    .filter(f => f.startsWith(date) && f.endsWith('.md') && !f.startsWith('discord-channels'))
    .map(f => ({
      name: f,
      path: path.join(MEMORY_DIR, f),
      mtime: fs.statSync(path.join(MEMORY_DIR, f)).mtime.getTime()
    }))
    .sort((a, b) => b.mtime - a.mtime);
  
  let content = '';
  
  // 讀最新既 memory file
  if (files.length > 0) {
    content = fs.readFileSync(files[0].path, 'utf8');
    console.log(`📖 Using memory file: ${files[0].name}`);
  }
  
  // 讀 Discord channels file
  const discordPath = path.join(MEMORY_DIR, `discord-channels-${date}.md`);
  if (fs.existsSync(discordPath)) {
    const discordContent = fs.readFileSync(discordPath, 'utf8');
    if (discordContent) {
      content += '\n\n## Discord Channels\n' + discordContent;
    }
  }
  
  return content || null;
}

/**
 * 用 Ollama (qwen2.5:3b) 生成日记
 */
function generateJournalWithOllama(date) {
  return new Promise((resolve, reject) => {
    // Find latest memory file for this date
    const files = fs.readdirSync(MEMORY_DIR)
      .filter(f => f.startsWith(date) && f.endsWith('.md') && !f.startsWith('discord-channels'))
      .map(f => ({
        name: f,
        path: path.join(MEMORY_DIR, f),
        mtime: fs.statSync(path.join(MEMORY_DIR, f)).mtime.getTime()
      }))
      .sort((a, b) => b.mtime - a.mtime);
    
    if (files.length === 0) {
      console.log('⚠️ No memory file found, no journal generated');
      resolve({ entry1: '', entry2: '', entry3: '' });
      return;
    }
    
    const memoryPath = files[0].path;
    const content = fs.readFileSync(memoryPath, 'utf8');
    console.log(`📖 Using memory file for AI: ${files[0].name}`);
    
    // Limit content length (increased for more detailed output)
    const truncatedContent = content.substring(0, 12000);
    
    const prompt = `你係AI助手。根據以下工作記錄，寫3段日記（總共800-1000字）：

1) 今日做咗乜 - 詳細列出主要事項（要有具體項目名稱、進度、點解決問題）
2) 學到乜 - 總結今日既學習同發現（要有具體例子、反思）
3) 聽日計劃 - 簡述聽日既優先事項同目標

格式要求：
- 用繁體中文（廣東話語法）
- 每段約250-350字，要有深度
- 要有具體內容，唔好太籠統
- 加入適量既emoji

記錄：${truncatedContent}

請直接輸出三段文字，每段用換行分隔，總共800-1000字：`;

    const postData = JSON.stringify({
      model: 'qwen2.5:3b',
      prompt: prompt,
      options: { num_predict: 2500, temperature: 0.7 },
      stream: false
    });

    const options = {
      hostname: 'localhost',
      port: 11434,
      path: '/api/generate',
      method: 'POST',
      headers: { 'Content-Type': 'application/json' }
    };

    const req = http.request(options, (res) => {
      let data = '';
      res.on('data', (chunk) => data += chunk);
      res.on('end', () => {
        try {
          const result = JSON.parse(data);
          const response = result.response || '';
          const paragraphs = response.split(/\n\n|\n/).filter(p => p.trim());
          
          if (paragraphs.length >= 2) {
            console.log('✅ Journal generated by Ollama (qwen2.5:3b)');
            resolve({
              entry1: paragraphs[0]?.trim() || '',
              entry2: paragraphs[1]?.trim() || '',
              entry3: paragraphs[2]?.trim() || ''
            });
          } else {
            console.log('⚠️ Ollama output not in expected format');
            resolve({ entry1: '', entry2: '', entry3: '' });
          }
        } catch (e) {
          console.log('⚠️ Ollama parse error:', e.message);
          resolve({ entry1: '', entry2: '', entry3: '' });
        }
      });
    });

    req.on('error', (e) => {
      console.log('⚠️ Ollama request failed:', e.message);
      resolve({ entry1: '', entry2: '', entry3: '' });
    });
    
    req.write(postData);
    req.end();
  });
}

/**
 * 用 MiniMax AI 生成个性化日记 (SIMPLIFIED)
 */
function generateJournalWithMiniMax(date) {
  return new Promise((resolve, reject) => {
    // 搵 latest memory file for this date
    const files = fs.readdirSync(MEMORY_DIR)
      .filter(f => f.startsWith(date) && f.endsWith('.md') && !f.startsWith('discord-channels'))
      .map(f => ({
        name: f,
        path: path.join(MEMORY_DIR, f),
        mtime: fs.statSync(path.join(MEMORY_DIR, f)).mtime.getTime()
      }))
      .sort((a, b) => b.mtime - a.mtime);
    
    if (files.length === 0) {
      console.log('⚠️ No memory file found, no journal generated');
      resolve({ entry1: '', entry2: '', entry3: '' });
      return;
    }
    
    const memoryPath = files[0].path;
    const content = fs.readFileSync(memoryPath, 'utf8');
    console.log(`📖 Using memory file for AI: ${files[0].name}`);
    
    // 限制 content 長度，避免 timeout
    const truncatedContent = content.substring(0, 8000);
    
    const prompt = `你係一個 AI 私人秘書助手。請根據以下今日既 Memory 記錄，幫我寫三段「個人日記」風格既文字，反映今日既工作同感受。

要求：
1. 第一段：今日主要做咗啲乜
2. 第二段：學到啲乜 / 反思  
3. 第三段：對明日既展望

用繁體中文寫，語氣自然誠恳，就像真係寫緊日記咁。唔洗列表，直接寫三段文字。

以下係今日既 Memory 記錄：
${truncatedContent}

請直接輸出三段文字，每段用換行分隔，別加任何標題或額外說明：`;

    const apiKey = process.env.MINIMAX_API_KEY;
    if (!apiKey) {
      console.log('⚠️ MINIMAX_API_KEY not set, skipping MiniMax generation');
      resolve({ entry1: '', entry2: '', entry3: '' });
      return;
    }
    
    const postData = JSON.stringify({
      model: 'MiniMax-M2.5',
      messages: [
        { role: 'user', content: prompt }
      ],
      temperature: 0.7
    });

    const options = {
      hostname: 'api.minimax.io',
      port: 443,
      path: '/v1/chat/completions',
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': 'Bearer ' + apiKey
      }
    };

    const req = https.request(options, (res) => {
      let data = '';
      res.on('data', (chunk) => data += chunk);
      res.on('end', () => {
        try {
          const result = JSON.parse(data);
          const response = result.choices?.[0]?.message?.content || '';
          
          const paragraphs = response.split(/\n\n|\n/).filter(p => p.trim());
          
          if (paragraphs.length >= 3) {
            console.log('✅ Journal generated by MiniMax');
            resolve({
              entry1: paragraphs[0].trim(),
              entry2: paragraphs[1].trim(),
              entry3: paragraphs[2].trim()
            });
          } else {
            console.log('⚠️ MiniMax output not in expected format');
            resolve({ entry1: '', entry2: '', entry3: '' });
          }
        } catch (e) {
          console.log('⚠️ MiniMax parse error:', e.message);
          resolve({ entry1: '', entry2: '', entry3: '' });
        }
      });
    });

    req.on('error', (e) => {
      console.log('⚠️ MiniMax request failed:', e.message);
      resolve({ entry1: '', entry2: '', entry3: '' });
    });
    
    req.write(postData);
    req.end();
  });
}

