function parseSummary(content) {
  const summary = {
    workSummary: "• 處理日常查詢同協作\n• 維護自動化系統\n• 協助 Josh 日常事務",
    learnings: "• 持續學習同優化\n• 改進工作流程",
    problems: "• 今日無重大問題",
    improvements: "• 持續優化系統",
    plans: "• 繼續協助 Josh 處理事務"
  };
  
  if (!content) return summary;
  
  // 簡單解析 - 搵關鍵字
  const lines = content.split('\n');
  const workItems = [];
  const learningItems = [];
  
  for (const line of lines) {
    if (line.includes('###') || line.startsWith('##')) continue;
    if (line.includes('Ollama') || line.includes('cron') || line.includes('job')) {
      if (!workItems.includes('系統維護同優化')) workItems.push('系統維護同優化');
    }
    if (line.includes('SSH') || line.includes('security') || line.includes('安全')) {
      if (!workItems.includes('安全升級')) workItems.push('安全升級');
    }
    if (line.includes('天氣') || line.includes('weather')) {
      if (!workItems.includes('天氣預報系統')) workItems.push('天氣預報系統');
    }
    if (line.includes('學習') || line.includes('記住')) {
      if (!learningItems.includes('持續學習新知識')) learningItems.push('持續學習新知識');
    }
  }
  
  if (workItems.length > 0) summary.workSummary = workItems.slice(0, 3).map(i => `• ${i}`).join('\n');
  if (learningItems.length > 0) summary.learnings = learningItems.slice(0, 3).map(i => `• ${i}`).join('\n');
  
  return summary;
}

async function generateDailySummary() {
  const date = getTodayDate();
  const displayDate = getDisplayDate(date);
  
  console.log(`📅 Generating daily summary for ${displayDate}...`);
  
  // 直接讀今日既 memory file
  const logContent = readTodayMemory(date);
  
  // 解析內容
  const summary = parseSummary(logContent);
  
  // 生成個人日記 (用 Ollama qwen2.5:3b)
  let journal = await generateJournalWithOllama(date);
  
  // 如果 AI 生成失敗，用 default
  if (!journal.entry1) {
    console.log('⚠️ Using default journal');
    journal = generateDefaultJournal();
  }
  
  // 檢查是否已存在
  const noteTitle = `AI 每日總結 - ${displayDate}`;
  if (checkNoteExists(noteTitle, "Ally's Daily")) {
    console.log(`⏭️ Daily summary for ${displayDate} already exists, skipping`);
    
    // ====== 仍然輸出到 stdout (俾 Cron Announce 用) ======
    console.log('\n' + '='.repeat(40));
    console.log(`📔 ${displayDate} 每日日記`);
    console.log('='.repeat(40));
    if (journal.entry1) console.log(`\n✍️ ${journal.entry1}`);
    if (journal.entry2) console.log(`\n💡 ${journal.entry2}`);
    if (journal.entry3) console.log(`\n🎯 ${journal.entry3}`);
    console.log('='.repeat(40));
    // ====== End ======
    return;
  }
  
  // 創建 Apple Note
  const success = createDailySummary(displayDate, summary, journal);
  
  if (success) {
    console.log(`✅ Daily summary created for ${displayDate}`);
    
    // ====== 輸出到 stdout (俾 Cron Announce 用) ======
    console.log('\n' + '='.repeat(40));
    console.log(`📔 ${displayDate} 每日日記`);
    console.log('='.repeat(40));
    if (journal.entry1) console.log(`\n✍️ ${journal.entry1}`);
    if (journal.entry2) console.log(`\n💡 ${journal.entry2}`);
    if (journal.entry3) console.log(`\n🎯 ${journal.entry3}`);
    console.log('='.repeat(40));
    // ====== End ======
    
    // Cron job 會負責發送到 Discord，唔洗重複
    
    // 記錄到日誌
    const logEntry = {
      timestamp: new Date().toISOString(),
      date: date,
      displayDate: displayDate,
      success: true,
      hasContent: !!logContent
    };
    
    const logPath = path.join(MEMORY_DIR, 'summary-log.json');
    let logs = [];
    if (fs.existsSync(logPath)) {
      logs = JSON.parse(fs.readFileSync(logPath, 'utf8'));
    }
    logs.push(logEntry);
    logs = logs.slice(-30);
    fs.writeFileSync(logPath, JSON.stringify(logs, null, 2));
    
  } else {
    console.error(`❌ Failed to create daily summary for ${displayDate}`);
  }
  
  return success;
}

